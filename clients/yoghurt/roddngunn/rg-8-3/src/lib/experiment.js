var waitForEl = function (selector, callback) {
  let count = 0;
  if (document.querySelector(selector) != null) {
    callback();
  } else if (count < 100) {
    setTimeout(() => {
      waitForEl(selector, callback, ++count);
    }, 100);
  }
};

